void main() {
  // Data types in Dart

  // Numbers, Strings, Booleans Lists, Maps, Dynamic

  // Strings
  String firstName = "Joe";
  print("String: $firstName");

  // Integers
  int age = 23;
  print("Interger: $age");

  // Doubles
  double otherNum = 19.95;
  print("Double: $otherNum");

  // Booleans
  bool myBool = false;
  print("Boolean: $myBool");

  // Dynamic
  dynamic fullName = "John Doe";
  print("Dynamic: $fullName");
}
